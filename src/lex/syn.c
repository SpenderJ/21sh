#include "msh.h"

inline int	sh_lex_syn(t_sh_tok *tok, char **ln)
{
	while (**ln == ' ' || **ln == '\t')
		++*ln;
	if (ft_strchr("=!()-;[]{|}\n", **ln))
	{
		tok->val = *ln;
		tok->id = (uint8_t)*(*ln)++;
		tok->len = 1;
		return (YEP);
	}
	return (NOP);
}
